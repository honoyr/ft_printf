#include "libft.h"

int		ft_strcheck(char *s, int c)
{
	int		i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] != '\0' && s[i] == (char)c)
			return (0);
		i++;
	}
	return (1);
}
