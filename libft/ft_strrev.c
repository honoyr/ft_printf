char	*ft_strrev(char *str)
{
	int		i;
	int		n;
	int		x;
	char	c;

	i = 0;
	n = 0;
	while (str[i] != '\0')
		i++;
	x = i / 2;
	i--;
	while (i >= x)
	{
		c = str[i];
		str[i] = str[n];
		str[n] = c;
		i--;
		n++;
	}
	return (str);
}
